static volatile uint8_t sampling_offset = 0;
static volatile uint16_t sampling_buffer_l[8] = { 0, 0, 0, 0, 0, 0, 0, 0 };
static volatile uint16_t sampling_buffer_r[8] = { 0, 0, 0, 0, 0, 0, 0, 0 };
