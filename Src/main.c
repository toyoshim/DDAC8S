uint16_t GetStabilizedSample(volatile uint16_t* samples) {
  // 1. if samples[0] == samples[1], then take samples[0]
  // 2. else if samples[1] == samples[2], then take samples[1]
  // 3. otherwise, samples[1] is racy, then takes samples[0]
  if (samples[1] == samples[2])
    return samples[1];
  return samples[0];
}

uint16_t AdjustL(uint16_t v) {
  // 12 11 10 9 3 2 1 0
  uint16_t lower = v & 0x000f;
  uint16_t upper = (v & 0x1e00) >> 5;
  return upper | lower;
}

uint16_t AdjustR(uint16_t v) {
  // 8 7 6 5 4 3 1 0
  uint16_t lower = v & 0x0003;
  uint16_t upper = (v & 0x01f8) >> 1;
  return upper | lower;
}

void HAL_I2S_TxHalfCpltCallback(I2S_HandleTypeDef *hi2s) {
  buffer[0] = (
      AdjustL(GetStabilizedSample(&sampling_buffer_l[0])) +
      AdjustL(GetStabilizedSample(&sampling_buffer_l[3]))) << 6;
  buffer[1] = (
      AdjustR(GetStabilizedSample(&sampling_buffer_r[0])) +
      AdjustR(GetStabilizedSample(&sampling_buffer_r[3]))) << 6;
  //buffer[0] = AdjustL(GetStabilizedSample(sampling_buffer_l)) << 7;
  //buffer[1] = AdjustR(GetStabilizedSample(sampling_buffer_r)) << 7;
  sampling_offset = 0;
}

